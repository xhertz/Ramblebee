CREATE TABLE IF NOT EXISTS `pub_C_geo_id` (
 ID int(9) NOT NULL AUTO_INCREMENT,
  lang varchar(10) COLLATE utf8_bin NOT NULL,
  Title varchar(160) COLLATE utf8_bin DEFAULT NULL,
  lat  double NOT NULL DEFAULT '0',
  lon  double NOT NULL DEFAULT '0',
  types varchar(50) COLLATE utf8_bin NOT NULL,
  pop varchar(50) COLLATE utf8_bin NOT NULL,
  `Height` varchar(50) COLLATE utf8_bin NOT NULL,
  `Country` varchar(10) COLLATE utf8_bin NOT NULL,
  `Subregion` varchar(10) COLLATE utf8_bin NOT NULL,
  Scale varchar(10) COLLATE utf8_bin NOT NULL,
  dim varchar(30) COLLATE utf8_bin NOT NULL,
  psize  double NOT NULL DEFAULT '0',
  style varchar(50) COLLATE utf8_bin NOT NULL,
  type varchar(10) COLLATE utf8_bin NOT NULL,
  image varchar(160) COLLATE utf8_bin DEFAULT NULL,
  imagejpg varchar(160) COLLATE utf8_bin DEFAULT NULL,
  `name` varchar(160) COLLATE utf8_bin DEFAULT NULL,
  page_id  double NOT NULL DEFAULT '0',
  `T_en` varchar(160) COLLATE utf8_bin DEFAULT NULL,
  `T_en-simple` varchar(160) COLLATE utf8_bin DEFAULT NULL,
  `T_fr` varchar(160) COLLATE utf8_bin DEFAULT NULL,
  `T_map-bms` varchar(160) COLLATE utf8_bin DEFAULT NULL,
  the_geom varchar(160) COLLATE utf8_bin DEFAULT NULL,
 PRIMARY KEY (`ID`),
 UNIQUE KEY `T_en` (`T_en`),
 KEY `t` (`pop`),
 KEY `type` (`type`),
 KEY `Character` (`psize`),
 KEY `lang` (`lang`),
 KEY `lon` (`lon`),
 KEY `lat` (`lat`),
 KEY `image` (`image`),
 KEY `Country` (`Country`),
 KEY `Subregion` (`Subregion`),
 KEY `Title` (`Title`),
 KEY `imagejpg` (`imagejpg`),
 KEY `name` (`name`),
 KEY `dim` (`dim`)
) ENGINE=InnoDB  DEFAULT CHARSET=utf8 COLLATE=utf8_bin